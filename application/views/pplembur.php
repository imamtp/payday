<p>Note: Angka 1/173 didasarkan pada perhitungan sbb:<br />
  Dalam satu tahun&nbsp; ada&nbsp; 52 minggu<br />
  1 bulan =&nbsp; 52/12&nbsp; = 4,33 minggu.<br />
  Total jam kerja/minggu = 40 jam<br />
  Jadi&nbsp; Total jam kerja dalam 1 bulan =&nbsp; 40 X 4,33&nbsp; =&nbsp; 173,33  dibulatkan menjadi 173 jam maka&nbsp; untuk menghitung upah per jam yaitu upah  perbulan / 173.</p>
